public class switchIntro {

  public static void main(String[] args) {
    int num = 10;
    switch (num) {
      case 10:
        System.out.println("Hello");

        break;
      case 5:
        System.out.println("OK");
        break;
      case 7:
        System.out.println("Khoke");
        break;

      default:
        break;
    }

  }
}
