public class bitwise {

  public static void main(String[] args) {

    int a = 2; // 10
    int b = 3; // 11

    // AND & Operator
    System.out.println(a & b); // 10 -> 2

    // OR | Operator
    System.out.println(a | b); // 11 -> 3

    // NOT ~ Operator
    System.out.println(~a); // 1101 -> -3

    // XOR ^ Operator
    System.out.println(a ^ b); // 01 -> 1

    // Left Shift Operator
    System.out.println(5 << 1); // 1010 -> 10

    // Right Shift Operator
    System.out.println(); 
  }
}
